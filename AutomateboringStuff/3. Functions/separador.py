# Já sabemos de algumas funções que o print()
# possui, agora podemos entrar na relação de
# separadores de uma mesma linha

# Para isso iremos fazer o seguinte comando

print('Cats', 'Dogs', 'Mice')
# Deve imprimir todas as strings separadas
# por um espaço

# Agora se redefinirmos da seguinte maneira

print('Cats', 'Dogs', 'Mice', sep=',')
# Então agora eles devem estar separados por
# uma vírgula
